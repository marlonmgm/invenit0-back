module.exports = {

  tableName: 'groups',

  attributes: {

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    name: {
      type: 'string',
      required: true
    },

    description: {
      type: 'string'
    },

    active: {
      type: 'boolean',
      defaultsTo: true
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝
    permissions: {
      collection:'permission',
      via: 'group'
    },

    usuarios: {
      collection: 'User',
      via: 'group'
    }

  },

  beforeDestroy: async function(criteria, next){ //Eliminar permisos antes de eliminar el grupo
    criteria.select = ['id'];
    let registros = await Group.find(criteria);
    let ids= [];
    _.forEach(registros, (r) => ids.push(r.id));
    await Permission.destroy({ group: { in: ids } });
    return next();
  }

};
